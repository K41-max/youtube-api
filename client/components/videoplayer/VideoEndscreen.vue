<template>
  <div v-if="videoElement" class="video-endscreen" :class="{ 'card-hover': hover }">
    <EndscreenCard
      v-for="(card, index) in endscreenData"
      :key="index"
      :card="card"
      :videoProgress="videoProgress"
      :videoHeight="videoElement.clientHeight"
      :videoWidth="videoElement.clientWidth"
      @cardenter="onCardEnter"
      @cardleave="onCardLeave"
    />
  </div>
</template>

<script lang="ts">
import EndscreenCard from '@/components/videoplayer/EndscreenCard.vue';

import Vue from 'vue';

export default Vue.extend({
  name: 'VideoEndscreen',
  components: {
    EndscreenCard
  },
  props: {
    videoId: String,
    videoProgress: Number,
    videoElement: null
  },
  data: () => ({
    endscreenData: [],
    hover: false
  }),
  mounted() {
    fetch(
      `${this.$store.getters['environment/apiUrl']}video/getEndscreen.php?videoId=${this.videoId}`,
      {
        cache: 'force-cache',
        method: 'GET'
      }
    )
      .then(response => {
        if (response.ok) {
          return response.json();
        }
      })
      .then(data => {
        this.endscreenData = data;
      })
      .catch(_ => {});
  },
  methods: {
    onCardEnter() {
      this.hover = true;
    },
    onCardLeave() {
      this.hover = false;
    }
  }
});
</script>

